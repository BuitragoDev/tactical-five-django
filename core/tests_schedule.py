class ScheduleGameCountTests(TestCase):
    """
    Tests for the number of games per team.
    Each team should play exactly 82 games in regular season.
    """

    def _calculate_team_games(self, teams, matchups):
        """Count games per team from matchups list."""
        game_count = {t.pk: 0 for t in teams}
        for home_pk, away_pk in matchups:
            game_count[home_pk] = game_count.get(home_pk, 0) + 1
            game_count[away_pk] = game_count.get(away_pk, 0) + 1
        return game_count

    def test_each_team_plays_82_games(self):
        """Each team should play exactly 82 games"""
        # This simulates what the schedule generator does
        teams = list(range(30))  # 30 teams
        matchups = []  # Would be generated by schedule generator
        
        # Verify the constant
        self.assertEqual(82, 82)  # This is the expected game count

    def test_total_games_calculation(self):
        """Total games in league = teams * games / 2 (each game involves 2 teams)"""
        teams = 30
        games_per_team = 82
        total_games = teams * games_per_team / 2
        self.assertEqual(total_games, 1230)


class ScheduleDivisionMatchupsTests(TestCase):
    """
    Tests for division matchup logic.
    Teams in the same division play each other 4 times.
    """

    def _div_pair_games(self, teams_a, teams_b):
        """Replicate the division pair games logic."""
        matchups = []
        n = len(teams_a)
        for i, ta in enumerate(teams_a):
            for j, tb in enumerate(teams_b):
                offset = (j - i) % n
                count = 4 if offset < 3 else 3
                for _ in range(count):
                    matchups.append((ta, tb))
        return matchups

    def test_division_pair_produces_games(self):
        """Division pairs should produce games"""
        teams_a = ['A1', 'A2', 'A3']
        teams_b = ['B1', 'B2', 'B3']
        matchups = self._div_pair_games(teams_a, teams_b)
        self.assertGreater(len(matchups), 0)

    def test_each_team_plays_4_times_in_division(self):
        """Each team should play division opponents 4 times"""
        teams_a = ['A1', 'A2', 'A3']  # 3 teams
        teams_b = ['B1', 'B2', 'B3']  # 3 teams
        
        # Count games per team
        matchups = self._div_pair_games(teams_a, teams_b)
        a1_count = sum(1 for m in matchups if m[0] == 'A1')
        
        # A1 plays B1, B2, B3 multiple times
        # offset=0: plays 4 times each
        # Total should be multiple of 3 (one for each opponent in other division)
        self.assertGreater(a1_count, 0)


class ScheduleConferenceMatchupsTests(TestCase):
    """
    Tests for conference matchup logic.
    Teams in same conference but different divisions play 3-4 times.
    """

    def test_same_division_teams_play_4_times(self):
        """Teams in same division play each other 4 times"""
        # Within a division, teams play 4 times each
        # For example, in Atlantic: BOS, BKN, NYK, PHI, TOR play each other 4x
        teams_in_division = 5  # Atlantic has 5 teams
        games_per_pair = 4
        
        # Total division games = C(5,2) * 4 = 10 * 4 = 40 games
        # C(n,2) = n! / (2! * (n-2)!)
        import math
        total_pairs = math.comb(teams_in_division, 2)
        total_games = total_pairs * games_per_pair
        
        self.assertEqual(total_games, 40)


